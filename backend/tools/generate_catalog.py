def group_templates(templates: list[dict[str, Any]]) -> dict[str, list[dict[str, Any]]]:
    grouped: dict[str, list[dict[str, Any]]] = defaultdict(list)
    for template in templates:
        grouped[template["group"]].append(template)
    return grouped


def write_group_files(grouped: dict[str, list[dict[str, Any]]]) -> tuple[int, int]:
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
    file_count = 0
    template_count = 0

    for group_slug in sorted(grouped):
        templates = grouped[group_slug]
        output_path = OUTPUT_DIR / f"{group_slug}.yaml"
        payload = {"templates": templates}
        with output_path.open("w", encoding="utf-8") as handle:
            yaml.safe_dump(
                payload,
                handle,
                sort_keys=False,
                default_flow_style=False,
                allow_unicode=True,
                width=120,
            )
        file_count += 1
        template_count += len(templates)

    return file_count, template_count


def main() -> None:
    grouped = group_templates(TEMPLATES)
    file_count, template_count = write_group_files(grouped)
    print(f"Generated {file_count} catalog files with {template_count} templates.")
    print(f"Output directory: {OUTPUT_DIR}")


if __name__ == "__main__":
    main()
