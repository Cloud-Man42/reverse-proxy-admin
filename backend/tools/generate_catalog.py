OPTIMIZED_FORWARD_HEADERS = [
    {"name": "X-Forwarded-Proto", "value": "https"},
    {"name": "Host", "value": "$host"},
]


def tpl(slug: str, name: str, group: str, category: str, **kwargs: Any) -> dict[str, Any]:
    """Build an application template dict with sensible defaults."""
    optimized = kwargs.pop("optimized", False)
    large_upload = kwargs.pop("large_upload_support", False)
    websocket = kwargs.pop("websocket_support", False)
    protocol = kwargs.pop("default_upstream_protocol", "http")
    port = kwargs.pop("default_upstream_port", 8080)
    availability = kwargs.pop("availability_level", "free")
    tags = kwargs.pop("tags", [])
    documentation_url = kwargs.pop("documentation_url", None)
    security_notes = kwargs.pop("security_notes", None)

    template: dict[str, Any] = {
        "slug": slug,
        "name": name,
        "description": kwargs.pop("description", f"Reverse proxy template for {name}."),
        "group": group,
        "category": category,
        "icon": kwargs.pop("icon", "app"),
        "tags": tags,
        "availability_level": availability,
        "optimized": optimized,
        "default_upstream_protocol": protocol,
        "default_upstream_port": port,
        "websocket_support": websocket,
        "large_upload_support": large_upload,
        "recommended_client_max_body_size": kwargs.pop("recommended_client_max_body_size", None),
        "recommended_proxy_read_timeout": kwargs.pop("recommended_proxy_read_timeout", None),
        "recommended_proxy_send_timeout": kwargs.pop("recommended_proxy_send_timeout", None),
        "recommended_proxy_connect_timeout": kwargs.pop("recommended_proxy_connect_timeout", None),
        "https_upstream_supported": kwargs.pop(
            "https_upstream_supported", protocol == "https"
        ),
        "http_to_https_redirect_default": kwargs.pop("http_to_https_redirect_default", True),
        "recommended_headers": kwargs.pop("recommended_headers", []),
        "security_headers": kwargs.pop("security_headers", []),
        "health_check_path": kwargs.pop("health_check_path", None),
        "rate_limit_recommendation": kwargs.pop("rate_limit_recommendation", None),
        "notes": kwargs.pop("notes", None),
        "security_notes": security_notes,
        "documentation_url": documentation_url,
        "long_description": kwargs.pop("long_description", None),
        "slug_aliases": kwargs.pop("slug_aliases", []),
        "hsts_recommended": kwargs.pop("hsts_recommended", False),
    }

    if large_upload and not template["recommended_client_max_body_size"]:
        template["recommended_client_max_body_size"] = "1G"

    if optimized:
        template["recommended_proxy_read_timeout"] = (
            template["recommended_proxy_read_timeout"] or "3600s"
        )
        template["recommended_proxy_send_timeout"] = (
            template["recommended_proxy_send_timeout"] or "3600s"
        )
        template["hsts_recommended"] = True
        if not template["recommended_headers"]:
            template["recommended_headers"] = deepcopy(OPTIMIZED_FORWARD_HEADERS)
        if not template["security_notes"]:
            template["security_notes"] = (
                f"Use extended proxy timeouts and forwarded headers for {name}. "
                "Restrict admin access to trusted networks."
            )

    template.update(kwargs)
    return template


